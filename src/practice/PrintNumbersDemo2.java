package practice;

public class PrintNumbersDemo2
{

	public static void main(String[] args) 
	{
	for(int a = 2; a<=100;a++)
	{
		System.out.println(a++);
		
		
	}
	
	for(int a =100; a<=150;a++)
	{
		System.out.println(a++);
	}
		
	for(int a = 100; a>=0;a--)
	{
	System.out.println(a--);
		
	}	
	for(int a = 150; a>=100;a--)
	{
		System.out.println(a--);
	}
		
		
		
		
		
		
		

	}

}
